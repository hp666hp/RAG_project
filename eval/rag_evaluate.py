def normalize_source(source: str | None) -> str:
    if not source:
        return ""
    return Path(str(source).strip().replace("/", "\\")).name.lower()


def ensure_api_key() -> None:
    if not settings.dashscope_api_key:
        raise RuntimeError("缺少 DASHSCOPE_API_KEY，先配置环境变量后再运行评测。")


def build_store() -> None:
    ensure_api_key()
    documents = load_documents(RAW_DATA_PATH)
    if not documents:
        raise RuntimeError(f"{RAW_DATA_PATH} 下没有可用文档，无法重建向量库。")
    chunks = split_documents(documents)
    if not chunks:
        raise RuntimeError("文档切分后为空，无法重建向量库。")
    reset_vector_store()
    add_documents_to_vector_store(chunks)
    print(f"已重建向量库：原始文档数={len(documents)}，切分块数={len(chunks)}")


def generate_dataset() -> list[dict[str, Any]]:
    samples: list[dict[str, Any]] = []
    for source_name, questions in QUESTION_BANK.items():
        for index, question in enumerate(questions, start=1):
            samples.append(
                {
                    "id": f"{source_name}::q{index}",
                    "question": question,
                    "gold_source": source_name,
                }
            )
    DATASET_PATH.write_text(json.dumps(samples, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"测试集已生成：{DATASET_PATH}，样本数={len(samples)}")
    return samples


def load_dataset() -> list[dict[str, Any]]:
    if not DATASET_PATH.exists():
        raise FileNotFoundError(f"测试集不存在：{DATASET_PATH}，请先生成测试集。")
    return json.loads(DATASET_PATH.read_text(encoding="utf-8"))


def collect_retrieved_sources(docs: list[Any]) -> list[str]:
    retrieved_sources: list[str] = []

    for doc in docs:
        metadata = getattr(doc, "metadata", {}) or {}
        source = normalize_source(metadata.get("source"))
        if source and source not in retrieved_sources:
            retrieved_sources.append(source)

    return retrieved_sources


def build_eval_retriever():
    return build_rerank_retriever(
        top_k=TOP_K,
        rerank_top_n=RERANK_TOP_N,
    )


def evaluate_dataset(samples: list[dict[str, Any]]) -> None:
    ensure_api_key()
    retriever = build_eval_retriever()
    total_tp = 0
    total_fp = 0
    total_fn = 0
    hit_count = 0
    miss_count = 0

    for item in samples:
        question = item["question"]
        gold_source = normalize_source(item["gold_source"])
        docs = retriever.invoke(question) or []

        retrieved_sources = collect_retrieved_sources(docs)

        hit = gold_source in retrieved_sources
        tp = 1 if hit else 0
        fp = len([source for source in retrieved_sources if source != gold_source])
        fn = 0 if hit else 1

        total_tp += tp
        total_fp += fp
        total_fn += fn
        hit_count += 1 if hit else 0
        miss_count += 0 if hit else 1

        print("-" * 80)
        print(f"问题: {question}")
        print(f"标准文件: {gold_source}")
        print(f"返回文件: {retrieved_sources}")
        print(f"是否命中: {'是' if hit else '否'}")

    precision = total_tp / (total_tp + total_fp) if (total_tp + total_fp) else 0.0
    recall = total_tp / (total_tp + total_fn) if (total_tp + total_fn) else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0

    print("=" * 80)
    print(f"样本总数: {len(samples)}")
    print(f"命中数: {hit_count}")
    print(f"未命中数: {miss_count}")
    print(f"TP: {total_tp}")
    print(f"FP: {total_fp}")
    print(f"FN: {total_fn}")
    print(f"Precision: {precision:.4f}")
    print(f"Recall: {recall:.4f}")
    print(f"F1: {f1:.4f}")


def main() -> None:
    action = "all"
    if len(sys.argv) > 1:
        action = sys.argv[1].strip().lower()

    if action == "build_store":
        build_store()
        return

    if action == "generate":
        generate_dataset()
        return

    if action == "evaluate":
        evaluate_dataset(load_dataset())
        return

    if action == "all":
        generate_dataset()
        evaluate_dataset(load_dataset())
        return

    raise ValueError("支持的命令：build_store / generate / evaluate / all")


if __name__ == "__main__":
    main()
